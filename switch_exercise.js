let day = "thursday";

switch (day) {
  case "monday":
    console.log("monday 123");
    break;
  case "tuesday":
    console.log("tuesday 123");
    break;
  case "wednesday":
    console.log("wednesday 123");
    break;
  case "thursday":
    console.log("thursday 123");
    break;
  case "friday":
    console.log("friday 123");
    break;
  case "saturday":
    console.log("saturday 123");
    break;
  case "sunday":
    console.log("sunday 123");
    break;
  default:
    console.log("NOTHING TO DO");
}
